plt.show()

"""2) Contribution of Top 5 States to Total Cases"""

# Calculate total cases across all states
total_cases = Rape_df['Total Cases'].sum()

# Top 5 states
top_states = Rape_df.nlargest(5, 'Total Cases')[['State_UT', 'Total Cases']]

# Calculate contribution of top 5 states
top_5_total = top_states['Total Cases'].sum()
contribution = (top_5_total / total_cases) * 100
print(f"Top 5 States Contribution to Total Cases: {contribution:.2f}%")

"""3) Average Number of Cases per Year per State"""

# Calculate average cases per year for each state
Rape_df['Average Cases/Year'] = Rape_df.iloc[:, 1:-2].mean(axis=1)
print("Average Cases Per Year by State:\n", Rape_df[['State_UT', 'Average Cases/Year']])

"""4) Percentage Change in Total Cases Over 2015-2020"""

Rape_df.columns

# Convert '2020_CR' and '2015_CR' columns to numeric type before calculation
Rape_df['2020_CR'] = pd.to_numeric(Rape_df['2020_CR'], errors='coerce')
Rape_df['2015_CR'] = pd.to_numeric(Rape_df['2015_CR'], errors='coerce')

# Calculate percentage change from 2015 to 2020
Rape_df['Percentage Change (2015-2020)'] = ((Rape_df[Rape_df['2020_CR']>0]['2020_CR'] - Rape_df[Rape_df['2015_CR']>0]['2015_CR']) / Rape_df[Rape_df['2015_CR']>0]['2015_CR']) * 100


# Display states with the highest and lowest change
highest_change = Rape_df.nlargest(5, 'Percentage Change (2015-2020)')
lowest_change = Rape_df.nsmallest(5, 'Percentage Change (2015-2020)')

print("States with Highest Increase in Cases:\n", highest_change[['State_UT', 'Percentage Change (2015-2020)']])
print("States with Lowest Increase in Cases:\n", lowest_change[['State_UT', 'Percentage Change (2015-2020)']])

