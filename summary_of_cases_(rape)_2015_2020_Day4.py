"""Key Insights and KPIs:

1) Year-over-Year Growth in Reported Cases
"""

# Calculate percentage growth year-over-year
yearly_cases = Rape_df.iloc[:, 1:7].sum()

yearly_growth = yearly_cases.pct_change() * 100


# Plot
plt.figure(figsize=(10, 5))
yearly_growth.plot(kind='bar', color='lightgreen')
plt.title("Year-over-Year Growth in Cases (2015-2020)")
plt.xlabel("Year")
